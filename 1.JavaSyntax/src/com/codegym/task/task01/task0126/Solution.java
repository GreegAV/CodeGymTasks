package com.codegym.task.task01.task0126;

/* 
Don't feel like it? Do it anyway.
Write a program that displays: "If you feel like it, do the task. If you don't feel like it, do it anyway" 16 times.


Requirements:
1. The program should output text.
2. The text should begin with "If you feel like it".
3. The text should end with "anyway".
4. The text should consist of 16 lines.
5. The displayed text must match the task conditions.

*/

public class Solution {
    public static void main(String[] args) {
        //write your code here
        for (int i = 0; i < 16; i++) {
            System.out.println("If you feel like it, do the task. If you don't feel like it, do it anyway");
        }
    }
}
